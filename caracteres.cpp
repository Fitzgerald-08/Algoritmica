#include <stdio.h>

int main(){
	printf("Hola Mundo");
	printf("\nHola Mundo"); /* En esta linea de codigo, el caracter '\n' representa un salto de linea */
	printf("\nX\nY");
	printf("\n\nHola Mundo\a"); /* El caracter '\a' significa una alerta que se muestra en forma de sonido */
	printf("\n\n\n\"Hola Mundo\""); /* Este caracter ' \" ', imprime las comillas dobles, sin el slash hacia atras, arrojaria  */
}
